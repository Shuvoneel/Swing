    public boolean validateEmail(String email) {
        int atpos = email.indexOf("@");
        int dotpos = email.lastIndexOf(".");
        if (atpos > 1 && (dotpos - atpos) > 2) {
            return true;
        } else {
            return false;
        }
    }

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (Integer.parseInt(txtId.getText()) < 2) {
            JOptionPane.showMessageDialog(null, "Enter your ID");
        } else if (txtName.getText().trim().length() < 3) {
            JOptionPane.showMessageDialog(null, "Enter your Name");
        } else if (Integer.parseInt(txtAge.getText()) < 18 || Integer.parseInt(txtAge.getText()) > 50) {
            JOptionPane.showMessageDialog(null, "Enter your Age");
        } else if (!validateEmail(txtEmail.getText())) {
            JOptionPane.showMessageDialog(null, "Enter your valid Email");
        } else if (!rMale.isSelected() && !rFemale.isSelected()) {
            JOptionPane.showMessageDialog(null, "Select your Gender");
        } else if (!chkOne.isSelected() && !chkTwo.isSelected() && !chkThree.isSelected()) {
            JOptionPane.showMessageDialog(null, "Select your Course");
        } else if (cmbRound.getItemAt(cmbRound.getSelectedIndex()).equalsIgnoreCase("select a round")) {
            JOptionPane.showMessageDialog(null, "Select your Round");
        } else if (txtMsg.getText().trim().length() < 7) {
            JOptionPane.showMessageDialog(null, "Enter your Comments");
        } else {
            int id = Integer.parseInt(txtId.getText());
            String name = txtName.getText();
            int age = Integer.parseInt(txtAge.getText());
            String email = txtEmail.getText();
            String gender = "";
            if (rMale.isSelected()) {
                gender = rMale.getText();
            }
            if (rFemale.isSelected()) {
                gender = rFemale.getText();
            }
            String course = "";
            if (chkOne.isSelected()) {
                course = chkOne.getText() + " ";
            }
            if (chkTwo.isSelected()) {
                course += chkTwo.getText() + " ";
            }
            if (chkThree.isSelected()) {
                course += chkThree.getText();
            }
            String round = cmbRound.getItemAt(cmbRound.getSelectedIndex());
            String msg = txtMsg.getText();

            DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
            Object[] cols = new Object[8];
            cols[0] = id;
            cols[1] = name;
            cols[2] = age;
            cols[3] = email;
            cols[4] = gender;
            cols[5] = course;
            cols[6] = round;
            cols[7] = msg;
            model.addRow(cols);

            Student student = new Student(id, name, age, email, gender, course, round, msg);
            List<Student> list = new ArrayList<>();
            list.add(student);

            Methods.writingMethod(JOptionPane.showInputDialog("Enter file name to save"), list);
        }
    }//GEN-LAST:event_btnAddActionPerformed

