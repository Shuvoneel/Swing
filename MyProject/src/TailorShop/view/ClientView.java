public void displayClientListIntoTable() {
        ClientDao dao = new ClientDaoImpl();
        List<Client> list = dao.getClients();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] cols = new Object[5];

        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getId();
            cols[1] = list.get(i).getName();
            cols[2] = list.get(i).getMobile();
            cols[3] = list.get(i).getEmail();
            cols[4] = list.get(i).getAddress();
            model.addRow(cols);
        }
    }
    private void btnRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegActionPerformed
        Client client = new Client(txtName.getText(), Integer.parseInt(txtMobile.getText()), txtEmail.getText(), txtAddress.getText());
        ClientDao obj = new ClientDaoImpl();
        obj.save(client);
        displayClientListIntoTable();
        JOptionPane.showMessageDialog(null, "Success !");
    }//GEN-LAST:event_btnRegActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Client client = new Client(Integer.parseInt(txtId.getText()), txtName.getText(), Integer.parseInt(txtMobile.getText()), txtEmail.getText(), txtAddress.getText());
        ClientDao obj = new ClientDaoImpl();
        obj.update(client);
        displayClientListIntoTable();
        JOptionPane.showMessageDialog(null, "Success !");
    }//GEN-LAST:event_btnUpdateActionPerformed

